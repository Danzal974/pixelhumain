	var dist = function(offset){
	  var x = center.left - offset.left;
	  var y = center.top - offset.top;
	  return Math.sqrt(x*x + y*y);
	}

	$('.hexagon').each(function(i,hexagon){
	  var hex = $(hexagon);
	  var distance = Math.round(dist(hex.offset())/50) + Math.round(Math.random() * 7);
